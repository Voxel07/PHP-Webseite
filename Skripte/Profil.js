function ProfilInfoToggel(){
    const schalter = document.getElementById("infoSchalter-Profil");
    console.log(schalter);
    if (schalter.style.display == "flex"){
        schalter.style.display="none";
    }
    else{
        schalter.style.display="flex";
    }
}
